  /**
   * Generate a ZK discrete-log proof.
   *
   * @param Q         - Public key point handle.
   * @param witness   - Secret witness (scalar bytes).
   * @param sessionId - Session identifier.
   * @param aux       - Auxiliary data (uint64).
   * @returns Proof bytes.
   */
  async zkDlProve(
    Q: PointHandle,
    witness: Uint8Array,
    sessionId: Uint8Array,
    aux: number,
  ): Promise<Uint8Array> {
    const qRef = this.wasm._malloc(4);
    this.wasm.setValue(qRef, Q as unknown as number, "*");
    const wCmem = writeCmem(this.wasm, witness);
    const sidCmem = writeCmem(this.wasm, sessionId);
    const proofCmem = this.wasm._malloc(8);

    const err = await this.wasm.ccall(
      "zk_dl_prove",
      "number",
      ["number", "number", "number", "number", "number"],
      [qRef, wCmem, sidCmem, aux, proofCmem],
      { async: true },
    ) as number;

    const proof = err === 0 ? readCmem(this.wasm, proofCmem) : new Uint8Array(0);

    this.wasm._free(qRef);
    freeCmem(this.wasm, wCmem);
    freeCmem(this.wasm, sidCmem);
    freeCmem(this.wasm, proofCmem);

    checkError(err, "zkDlProve");
    return proof;
  }

  /**
   * Verify a ZK discrete-log proof.
   *
   * @param Q         - Public key point handle.
   * @param proof     - Proof bytes (from zkDlProve).
   * @param sessionId - Session identifier (must match prover's).
   * @param aux       - Auxiliary data (must match prover's).
   * @returns true if the proof is valid.
   */
  async zkDlVerify(
    Q: PointHandle,
    proof: Uint8Array,
    sessionId: Uint8Array,
    aux: number,
  ): Promise<boolean> {
    const qRef = this.wasm._malloc(4);
    this.wasm.setValue(qRef, Q as unknown as number, "*");
    const proofCmem = writeCmem(this.wasm, proof);
    const sidCmem = writeCmem(this.wasm, sessionId);

    const result = await this.wasm.ccall(
      "zk_dl_verify",
      "number",
      ["number", "number", "number", "number"],
      [qRef, proofCmem, sidCmem, aux],
      { async: true },
    ) as number;

    this.wasm._free(qRef);
    freeCmem(this.wasm, proofCmem);
    freeCmem(this.wasm, sidCmem);

    return result === 0;
  }

