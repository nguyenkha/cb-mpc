    // Party name
    const nameCmem = this.wasm._malloc(8);
    this.wasm.ccall("mpc_eckey_mp_get_party_name", "number", ["number", "number"], [keyRef, nameCmem]);
    const nameData = readCmem(this.wasm, nameCmem);
    const partyName = new TextDecoder().decode(nameData);
    freeCmem(this.wasm, nameCmem);

    // Public key Q
