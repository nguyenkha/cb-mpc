// The Emscripten-generated factory function (imported at runtime).
// eslint-disable-next-line @typescript-eslint/no-var-requires
let createCbMpcFactory: (opts?: object) => Promise<CbMpcWasmModule>;

/**
 * Initialize the WASM module.
 *
 * @param wasmModuleFactory - The Emscripten module factory (from `require('./cbmpc.js')`
 *                            or `import('./cbmpc.js')`). If omitted, attempts dynamic import.
 * @returns A configured {@link CbMpc} instance ready for use.
 */
export async function initCbMpc(
  wasmModuleFactory?: (opts?: object) => Promise<CbMpcWasmModule>,
): Promise<CbMpc> {
  if (wasmModuleFactory) {
    createCbMpcFactory = wasmModuleFactory;
  } else if (!createCbMpcFactory) {
    // Dynamic import for the Emscripten glue
    // eslint-disable-next-line @typescript-eslint/ban-ts-comment
    // @ts-ignore -- cbmpc.js is generated by Emscripten at build time
    const mod = await import(/* webpackIgnore: true */ "../cbmpc.js");
    createCbMpcFactory = mod.default || mod.createCbMpc || mod;
  }

  const wasm = await createCbMpcFactory();

  // Initialize transport callback registry on the module
  (wasm as CbMpcWasmModule)._transportCallbacks = {};

  return new CbMpc(wasm);
}

// ---------------------------------------------------------------------------
// Internal helpers
// ---------------------------------------------------------------------------

/** Write a Uint8Array into WASM heap and return the pointer. Caller must free. */
function writeBytes(wasm: CbMpcWasmModule, data: Uint8Array): number {
  const ptr = wasm._malloc(data.length);
  wasm.HEAPU8.set(data, ptr);
  return ptr;
}

/** Read `size` bytes from WASM heap at `ptr` into a new Uint8Array. */
function readBytes(wasm: CbMpcWasmModule, ptr: number, size: number): Uint8Array {
  return new Uint8Array(wasm.HEAPU8.buffer, ptr, size).slice();
}

/** Allocate a cmem_t struct on the WASM heap and populate it. Returns pointer. */
function writeCmem(wasm: CbMpcWasmModule, data: Uint8Array): number {
  const cmemPtr = wasm._malloc(8); // cmem_t is {uint8_t* data, int size}
  const dataPtr = writeBytes(wasm, data);
  wasm.setValue(cmemPtr, dataPtr, "*");
  wasm.setValue(cmemPtr + 4, data.length, "i32");
  return cmemPtr;
}

/** Read a cmem_t from WASM heap and return its contents as Uint8Array. */
function readCmem(wasm: CbMpcWasmModule, cmemPtr: number): Uint8Array {
  const dataPtr = wasm.getValue(cmemPtr, "*");
  const size = wasm.getValue(cmemPtr + 4, "i32");
  if (!dataPtr || size <= 0) return new Uint8Array(0);
  return readBytes(wasm, dataPtr, size);
}

/** Free a cmem_t and its data buffer. */
function freeCmem(wasm: CbMpcWasmModule, cmemPtr: number): void {
  const dataPtr = wasm.getValue(cmemPtr, "*");
  if (dataPtr) wasm._free(dataPtr);
  wasm._free(cmemPtr);
}

/** Write a cmems_t (array of buffers) to the WASM heap. Returns pointer. */
function writeCmems(wasm: CbMpcWasmModule, buffers: Uint8Array[]): number {
  const count = buffers.length;
  let totalSize = 0;
  for (const b of buffers) totalSize += b.length;

  const dataPtr = wasm._malloc(totalSize);
  const sizesPtr = wasm._malloc(count * 4);
  let offset = 0;
  for (let i = 0; i < count; i++) {
    wasm.HEAPU8.set(buffers[i], dataPtr + offset);
    wasm.setValue(sizesPtr + i * 4, buffers[i].length, "i32");
    offset += buffers[i].length;
  }

  // cmems_t is {int count, uint8_t* data, int* sizes}
  const cmemsPtr = wasm._malloc(12);
  wasm.setValue(cmemsPtr, count, "i32");
  wasm.setValue(cmemsPtr + 4, dataPtr, "*");
  wasm.setValue(cmemsPtr + 8, sizesPtr, "*");
  return cmemsPtr;
}

/** Read a cmems_t from WASM heap into an array of Uint8Arrays. */
function readCmems(wasm: CbMpcWasmModule, cmemsPtr: number): Uint8Array[] {
  const count = wasm.getValue(cmemsPtr, "i32");
  const dataPtr = wasm.getValue(cmemsPtr + 4, "*");
  const sizesPtr = wasm.getValue(cmemsPtr + 8, "*");
  const result: Uint8Array[] = [];
  let offset = 0;
  for (let i = 0; i < count; i++) {
    const size = wasm.getValue(sizesPtr + i * 4, "i32");
    result.push(readBytes(wasm, dataPtr + offset, size));
    offset += size;
  }
  return result;
}

/** Free a cmems_t and its buffers. */
function freeCmems(wasm: CbMpcWasmModule, cmemsPtr: number): void {
  const dataPtr = wasm.getValue(cmemsPtr + 4, "*");
  const sizesPtr = wasm.getValue(cmemsPtr + 8, "*");
  if (dataPtr) wasm._free(dataPtr);
  if (sizesPtr) wasm._free(sizesPtr);
  wasm._free(cmemsPtr);
}

/** Allocate a ref struct (4 bytes, opaque pointer) and return its WASM address. */
function allocRef(wasm: CbMpcWasmModule): number {
  const ptr = wasm._malloc(4);
  wasm.setValue(ptr, 0, "*");
  return ptr;
}

/** Read the opaque pointer value from a ref struct. */
function readRef(wasm: CbMpcWasmModule, refPtr: number): number {
  return wasm.getValue(refPtr, "*");
}

/** Write a C string to WASM heap. Returns pointer. Caller must free. */
function writeCString(wasm: CbMpcWasmModule, str: string): number {
  const len = wasm.lengthBytesUTF8(str) + 1;
  const ptr = wasm._malloc(len);
  wasm.stringToUTF8(str, ptr, len);
  return ptr;
}

/** Check error code and throw if non-zero. */
function checkError(code: number, operation: string): void {
  if (code !== 0) {
    throw new CbMpcError(code, operation);
  }
}

// ---------------------------------------------------------------------------
// Error class
// ---------------------------------------------------------------------------

export class CbMpcError extends Error {
  constructor(
    public readonly code: number,
    public readonly operation: string,
  ) {
    super(`cb-mpc error ${code} during ${operation}`);
    this.name = "CbMpcError";
  }
}

